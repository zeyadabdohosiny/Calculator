package com.zizoabdohisinymohamed.calculator.MathmaticalOperations;

public class Mathmatical {
    // Variables
   static int result;


    // Sum

    public static int Sum(int x,int y){
        result=x+y;
        return result;
    }

   // subtraction

    public static int Subtraction(int x,int y){
        result=x-y;
        return result;
    }

    // multiplication

    public static int multiplication(int x,int y){

      result=x*y;
      return result;
    }

    public static int division(int x,int y){

        result=x/y;
        return result;
    }



}
